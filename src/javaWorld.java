// This program will output every even number from 2 through 200.
public class javaWorld {
    public static void main(String[] args){
        // Declarations
        int evenNumber = 2;
        int MAXNUMBER = 200;

        // Loop
        while(evenNumber <= MAXNUMBER) {
            System.out.println(evenNumber);
            evenNumber = evenNumber + 2;
        }





    }
}
