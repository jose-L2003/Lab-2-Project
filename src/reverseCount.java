// This program will output numbers in reverse order from 10 down to 0.
public class reverseCount {
    public static void main(String[] args) {
        // Declarations
        int number = 10;
        int ZERO = 0;

        // Loop
        while(number >= ZERO) {
            System.out.println(number);
            number--;
        }
        System.out.println("Done!");








    }
}

